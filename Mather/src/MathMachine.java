class MathMachine {

    int add(int x, int y) {
        return x + y;
    }

    int sub(int x, int y) {
        return x - y;
    }

    int mult(int x, int y) {
        return x * y;
    }

    double div(double x, double y) {
        return x / y;
    }

}
