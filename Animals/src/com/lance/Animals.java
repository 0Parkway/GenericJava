package com.lance;

public class Animals {
    int age, weight;
    String name;

    void setName(String name) {
        this.name = name;
    }

    void setAge(int age) {
        this.age = age;
    }

    void setWeight(int weight) {
        this.weight = weight;
    }

    String getName() {
        return this.name;
    }

    int getAge() {
        return this.age;
    }

    int getWeight() {
        return this.weight;
    }

}
