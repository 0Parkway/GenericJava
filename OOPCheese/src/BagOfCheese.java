public class BagOfCheese {
    String kind;
    double weight;
    int daysAged;
    boolean isDomestic;

    public BagOfCheese(String kind, double weight,
                       int daysAged, boolean isDomestic) {
        this.kind = kind;
        this.weight = weight;
        this.daysAged = daysAged;
        this.isDomestic = isDomestic;
    }
}
